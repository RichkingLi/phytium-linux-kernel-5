int te_ecp_group_init(const te_drv_handle hdl, te_ecp_group_t *grp)
{
    int ret = TE_SUCCESS;
    PK_CHECK_PARAM(grp);
    PK_CHECK_PARAM(hdl);

    aca_zeroize(grp, sizeof(te_ecp_group_t));

    grp->id = TE_ECP_DP_NONE;

    ret = te_ecp_point_init(hdl, &(grp->G));
    PK_CHECK_RET_GO;
    ret = te_bn_alloc(hdl, 0, &(grp->P));
    PK_CHECK_RET_GO;
    ret = te_bn_alloc(hdl, 0, &(grp->A));
    PK_CHECK_RET_GO;
    ret = te_bn_alloc(hdl, 0, &(grp->B));
    PK_CHECK_RET_GO;
    ret = te_bn_alloc(hdl, 0, &(grp->N));
    PK_CHECK_RET_GO;

    ret = TE_SUCCESS;
finish:
    if (TE_SUCCESS != ret) {
        te_ecp_point_free(&(grp->G));
        if (grp->P) {
            te_bn_free(grp->P);
            grp->P = NULL;
        }
        if (grp->A) {
            te_bn_free(grp->A);
            grp->A = NULL;
        }
        if (grp->B) {
            te_bn_free(grp->B);
            grp->B = NULL;
        }
        if (grp->N) {
            te_bn_free(grp->N);
            grp->N = NULL;
        }
    }
    return ret;
}

