void te_ecp_group_free(te_ecp_group_t *grp)
{
    if (!grp) {
        return;
    }
    te_ecp_point_free(&(grp->G));
    if (grp->P) {
        te_bn_free(grp->P);
        grp->P = NULL;
    }
    if (grp->A) {
        te_bn_free(grp->A);
        grp->A = NULL;
    }
    if (grp->B) {
        te_bn_free(grp->B);
        grp->B = NULL;
    }
    if (grp->N) {
        te_bn_free(grp->N);
        grp->N = NULL;
    }
    aca_zeroize(grp, sizeof(te_ecp_group_t));
}

int te_ecp_group_copy(te_ecp_group_t *dst, const te_ecp_group_t *src)
{

    int ret                 = TE_SUCCESS;
    te_drv_handle hdl = NULL;

    PK_CHECK_PARAM(dst && src && (src->id != TE_ECP_DP_NONE));
    PK_CHECK_FUNC(te_bn_get_drv_handle(src->P, &hdl));

    ret = te_ecp_group_load(dst, src->id);
    PK_CHECK_RET_GO;

finish:
    return ret;
}
