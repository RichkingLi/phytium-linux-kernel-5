/* convert endian in data */
static int _ecp_bn_load(te_bn_t *bn, const uint8_t *data, size_t size)
{
    int ret      = TE_SUCCESS;
    uint8_t *tmp = NULL;
    size_t i     = 0;
    uint8_t val  = 0;

    tmp = osal_malloc(size);
    PK_CHECK_COND_GO(tmp, TE_ERROR_OOM);

    /* convert little endian to big endian */
    for (i = 0; i < size / 2; i++) {
        val               = data[size - 1 - i];
        tmp[size - 1 - i] = data[i];
        tmp[i]            = val;
    }
    /* import to bn, always positive value */
    ret = te_bn_import(bn, (const uint8_t *)tmp, size, 1);
    PK_CHECK_RET_GO;

    ret = TE_SUCCESS;
finish:
    OSAL_SAFE_FREE(tmp);
    return ret;
}

/*
 * Make group available from embedded constants
 */
static int _ecp_group_load(te_ecp_group_t *grp,
                           const uint8_t *p,
                           size_t plen,
                           const uint8_t *a,
                           size_t alen,
                           const uint8_t *b,
                           size_t blen,
                           const uint8_t *gx,
                           size_t gxlen,
                           const uint8_t *gy,
                           size_t gylen,
                           const uint8_t *n,
                           size_t nlen)
{
    int ret        = TE_SUCCESS;
    int32_t A_sign = 0;

    ret = _ecp_bn_load(grp->P, p, plen);
    PK_CHECK_RET_GO;

    if (a != NULL) {
        ret = _ecp_bn_load(grp->A, a, alen);
        PK_CHECK_RET_GO;
    } else {
        /* a = p - 3 */
        ret = te_bn_sub_s32(grp->A, (const te_bn_t *)(grp->P), 3);
        PK_CHECK_RET_GO;

        ret = te_bn_get_sign(grp->A, &A_sign);
        PK_CHECK_RET_GO;
        PK_CHECK_COND_GO(A_sign == 1, TE_ERROR_NOT_ACCEPTABLE);
    }

    ret = _ecp_bn_load(grp->B, b, blen);
    PK_CHECK_RET_GO;
    ret = _ecp_bn_load(grp->N, n, nlen);
    PK_CHECK_RET_GO;

    ret = _ecp_bn_load(grp->G.X, gx, gxlen);
    PK_CHECK_RET_GO;
    ret = _ecp_bn_load(grp->G.Y, gy, gylen);
    PK_CHECK_RET_GO;
    ret = te_bn_import_s32(grp->G.Z, 1);
    PK_CHECK_RET_GO;

    ret = te_bn_bitlen(grp->P);
    PK_CHECK_COND_GO(ret >= 0, ret);
    PK_CHECK_COND_GO(ret != 0, TE_ERROR_BAD_INPUT_DATA);
    grp->pbits = (size_t)ret;

    ret = te_bn_bitlen(grp->N);
    PK_CHECK_COND_GO(ret >= 0, ret);
    PK_CHECK_COND_GO(ret != 0, TE_ERROR_BAD_INPUT_DATA);
    grp->nbits = (size_t)ret;

    ret = TE_SUCCESS;
finish:
    return ret;
}

